namespace xUnitTest.MathematicalOperations;
public class OperationService
{
    public int Add(int a, int b)
    {
        return a + b;
    }

    public int Subtract(int a, int b)
    {
        return a - b;
    }

    public int Multiply(int a, int b)
    {
        return a* b;
    }

    public decimal Divide (int a, int b)
    {
        return a / b;
    }
}
